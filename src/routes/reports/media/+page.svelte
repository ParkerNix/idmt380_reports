<h1>Youtube, Tiktok, and A.I. Transcription</h1>
<div class="section">
	<div class="section-card">
		<h2>Research Goal</h2>
		<p>
			In this report, I will be examining how well A.I. can transcribe spoken words in videos
			released by content creators. Making captions for videos used to be a big issue for content
			creators, who would rely on other people (oftentimes fans) to create them. Nowadays, A.I. can
			generate captions for you. That being said, I want to see if they can accurately understand
			and transcribe dialogue using various different content creators.
		</p>
	</div>
	<div class="section-card">
		<h2>Tool Researched</h2>
		<p>
			The A.I. I targeted for this task was Youtube's and Tiktok's transcribing A.I. These are two
			of the most popular places to find content on the internet, and both have these captions
			generated in an attempt to ensure all media on their site is accessible. This allows me to
			easily find the same video on both platforms, as content creators commonly post videos to
			both.
		</p>
	</div>
	<div class="section-card">
		<h2>Implementation and Testing</h2>
		<p>To examine both A.I., I followed these steps per test:</p>
		<ol>
			<li>
				<strong>Find a content creator's video</strong> on both Tiktok and Youtube.
			</li>
			<li>
				<strong>Watch the video</strong> with the captions on.
			</li>
			<li>
				<strong>Note the number of times mistakes are made</strong> in the captions along with what issues
				the A.I. had.
			</li>
			<li>
				<strong>Repeat for four videos,</strong> then find the average for each service
			</li>
		</ol>
	</div>
</div>

