	<div class="section-card">
		<h2>Conclusion</h2>
		<p>
			In the end, Both A.I. transcribers are about equally matched in terms of the amount of errors
			they make. However, I would say that the issues Youtube has with captions tend to be of a more
			severe magnitude than that of Tiktok. The delays that Youtube had cause certain sentences to
			be hard to follow. Both, however, could use training in slang and different speech styles.
			This is certainly an issue that has come up time and again about how A.I. is trained, and it
			seems like here it is not trained to work with CalebCity or Lenarr Young, both black content
			creators. However, more content creators would need to be researched to reach a better
			conclusion on that. Finally, it seems like these A.I. are fine with transcribing A.I.
			voiceovers. While this isn't terribly surprising, it does show that it recognizes the speech
			style those A.I. use.
		</p>
