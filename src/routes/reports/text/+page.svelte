		<h2>Conclusion</h2>
		<p>
			Both are at about the same quality at creating text, and there aren't any grammatical or
			spelling errors in the text. However, I did feel as though Grammarly specifically, but also
			LogicBalls, had issues trying to match the tone and/or formality of the prompt. In addition,
			LogicBalls can be quite long-winded, especially when asking for a more formal prompt. If i
			were to use either, I would probably go for Grammarly in general. They gave a much better
			length and I felt like it was a better starting point. LogicBalls and Grammarly both have paid
			features as well, so an interesting next step for this would be to see what happens with a
