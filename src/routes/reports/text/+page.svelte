		<h2>Research Goal</h2>
		<p>
			In this report, I will be examining two A.I. text content generators (Grammarly A.I. and
			LogicBalls' A.I. Content and Copy Generator) to see which is best at creating multi-paragraph
			text content. In today's world, taking alll the other parts of web design and development into
			account, the concept of content creation can be a bit of a hassle and can lead teams to
			putting information on their sites that may not have had the same work put into it as compared
			to things like colors, flows, etc. With A.I., we now have the option to create text content
			pretty reliably and quickly. To that point, this study seeks to find how these two A.I.
			compare to each other and if either one is better than the other
		</p>
	</div>
	<div class="section-card">
		<h2>Tool Researched</h2>
		<p>
			The A.I. I targeted for this task was Grammarly's A.I. generator and LogicBalls' A.I. Content
			and Copy Generator. Grammarly's A.I. can be activated right from their web extension, so I
			chose to use it with a Google Doc file. Meanwhile, Logicballs has its own web app for it's
			A.I., so it's a bit more inconvenient.
		</p>
	</div>
	<div class="section-card">
		<h2>Implementation and Testing</h2>
