		<h2><span>Comparative Analysis</span></h2>
		<p>
			<span
				>I&rsquo;m now going to look at two AI tools that have been used widely to help with
				development; ChatGPT and Codiumate. These two tools have been talked about widely in many
				web development communities and used for creating test cases and suggesting improvements to
				ensure a quality codebase. In this report, I&rsquo;ll be looking to see if there is a
				significant difference between the two and which, if either, of them could be a better
				option for writing better code.</span
			>
		</p>
		<h3><span>Definition of the Subjects</span></h3>
		<ul>
			<li>
				<span
					>ChatGPT: ChatGPT is a well-known prompt-based A.I. that can do many things, generating
					code included. It&rsquo;s not specifically made for coding, but has been widely used to
					improve the development process.</span
				>
			</li>
			<li>
				<span
					>Codiumate: Codiumate is a development-focused AI tool that seeks to help create test
					cases and suggest improvements to written code. It is an extension available for code
					editors like Visual Studio Code.</span
				>
			</li>
		</ul>
		<h3><span>Criteria</span></h3>
		<ul>
			<li>
				<span
					>Ease of Use: How easy is it to get an end product? Do you need an account? Are there any
					extra seteps or potential issues a user could have trying to get a solution?</span
				>
			</li>
			<li>
				<span
					>Suggesting: How many suggestions can it give? Can it critique various aspects of the code
					(readability, syntax, accessibility)?</span
				>
			</li>
			<li>
				<span
					>Test Cases: Can the tool make test cases? Are they expansive and test a large variety of
					potential issues?</span
				>
			</li>
		</ul>
		<h3><span>Comparison</span></h3>
		<p><strong>Ease of Use:</strong></p>
		<ul>
			<li>
				<span
					>ChatGPT: ChatGPT is not accessible from VSC, so there is a bit of window switching that
					has to happen. However, it is not necessary to have an account to use it, only to save
					chat logs. It will generate test cases</span
				>
			</li>
			<li>
				<span
					>Codiumate: As it is an extension for VSC, you can access it by simply highlighting your
					code, right clicking, and asking it to generate tests, summarize the code, and more. It
					can be set to make test cases for different packages (such as React Testing Library), but
					it must be set up in the repository and linked in the extension settings to run test
					cases. It can only generate them without it.</span
				>
			</li>
		</ul>
		<p><strong>Suggesting:</strong></p>
		<ul>
			<li>
				<span
					>ChatGPT: Upon prompting with code, it is able to give several suggestions (5 from one
					snippet function that loops through each item in one array and tries to find a matching
					string in another and deletes the string from both). It looks for improvements such as
					where comments can be added, where logic can be simplified, and where it can add in error
					handling.</span
				>
			</li>
			<li>
				<span
					>Codiumate: Codiumate will popup with a sidebar chat and give you several suggestions (4
					from the same prompt, but there is a button to generate more). From here, you can choose a
					suggestion to get more details on it, see a code preview, or hit a checkbox to have it
					implement the suggestion into a copy of your code, which can be reviewed and then put in
					palace of your original code.</span
				>
			</li>
		</ul>
		<p><strong>Test Cases:</strong></p>
		<ul>
			<li>
				<span
					>ChatGPT: It was able to generate several test cases (5 from one snippet function that
					added two numbers together), while also explaining the hooks it used (such as the
					&ldquo;beforeEach&rdquo; hook in React Testing Library). These were extensive and covered
					a variety of cases(adding negative numbers, adding 0, etc.).</span
				>
			</li>
			<li>
				<span
					>Codiumate: It was able to generate several test cases (2 from the same snippet). The
					first test is accurate and tests the main purpose of the function. The second test tries
					to use the function with an empty array, which is not something I would immediately think
					to test but is a good security measure.</span
				>
			</li>
		</ul>
		<h3><span>Conclusion</span></h3>
		<p>
			<span
				>When comparing ChatGPT and Codiumate in terms of assisting with coding, it is clear that
				both tools are strong contenders. There are a few nuances, however:</span
			>
		</p>
		<ul>
			<li>
				<span
					>ChatGPT may be more suited to someone who isn&rsquo;t as familiar with coding, as I feel
					it was a lot more explanatory and offered breakdowns of what, how, and why it used
					different hooks, methods, and so on. Being accessible from the web and many devices,
					developers can get help pretty much anywhere.</span
				>
			</li>
			<li>
				<span
					>Codiumate seems to be a better choice for developers who have a good handle on coding,
